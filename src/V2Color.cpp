uint8_t V2Color::toCIE1931(uint8_t v) {
  return CIE1931[v];
}

void V2Color::HSVtoRGB(float h, float s, float v, uint8_t &r, uint8_t &g, uint8_t &b) {
  if (h >= 360.f)
    h = 0;

  h /= 60.f;

  int8_t i = h;
  float f  = h - i;
  float p  = v * (1.f - s);
  float q  = v * (1.f - f * s);
  float t  = v * (1.f - (1.f - f) * s);

  switch (i) {
    case 0:
      r = v * 255.f;
      g = t * 255.f;
      b = p * 255.f;
      return;

    case 1:
      r = q * 255.f;
      g = v * 255.f;
      b = p * 255.f;
      return;

    case 2:
      r = p * 255.f;
      g = v * 255.f;
      b = t * 255.f;
      return;

    case 3:
      r = p * 255.f;
      g = q * 255.f;
      b = v * 255.f;
      return;

    case 4:
      r = t * 255.f;
      g = p * 255.f;
      b = v * 255.f;
      return;

    case 5:
      r = v * 255.f;
      g = p * 255.f;
      b = q * 255.f;
      return;
  }
}
